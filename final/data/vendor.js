const url = "mongodb://127.0.0.1:27017/Final";

async function create(vendorId, info, estimatePrice) {
    if(vendorId == undefined || info == undefined || estimatePrice == undefined)
        throw "parameters are missing.";
    var result = [];
    var promise = new Promise(function(resolve) {
        mongo.connect(url,(err, db) => {
            if(err) {
                throw "database connection failed!";
            }
            db.collection('vendor').insert({"vendorId": vendorId, "info": info, "estimatePrice": estimatePrice}, (err, res) => {
                if(err) {
                    db.close();
                    throw "insert error";
                }
                db.close();
                result = res["ops"];
                resolve(result);
            })
        })
    })
    promise.then(function(value) {
        return value;
    })
    return promise;
}

